#tipos de dados: str(string)
print(123+4) #tipagem dinamica reconhece como int
print("123+4") #tipagem dinamica reconhece tambem como str

#aspas simples
print('simples') #tipagem dinamica reconhece com str
print(1,'"simples"') #tipagem dinamica reconhece com str

#aspas duplas
print("duplas") #tipagem dinamica tambem reconhece como str
print(2,"'duplas'") #tipagem dinamica tambem reconhece como str

#\
print('tes\'te') #a \ é um caractere de escape, pula a proxíma caractere

#r
print(r'Allan \'puga\'') #uso do r mostra o escape


